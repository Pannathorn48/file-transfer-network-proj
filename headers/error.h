// ERR_SOCKET_FAIL : failed to create socket
#define ERR_SOCKET_FAIL -1

// ERR_INVALID_PORT : negative port number, port number equal 0, port number out of range
#define ERR_INVALID_PORT -2

// ERR_PORT_IN_USE : port is already binding to other process
#define ERR_PORT_IN_USE -3


// ERR_SEND_FAIL : failed to send data
#define ERR_SEND_FAIL -4

// ERR_RECV_FAIL : failed to receive data
#define ERR_RECV_FAIL -5

// ERR_FILE_NOT_FOUND : file not found
#define ERR_FILE_NOT_FOUND -6

// ERR_INVALID_HANDSHAKE : invalid handshake packet
#define ERR_INVALID_HANDSHAKE -7